void	ft_rev_int_tab(int *tab, int size)
{
	int	aux;
	int	i;

	i = 1;
	while (i <= size / 2)
	{
	aux = *(tab + size - i);
	*(tab + size - i) = *(tab + i - 1);
	*(tab + i - 1) = aux;
	i++;
	}
}
